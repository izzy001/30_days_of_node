//splice method can be used in two ways i.e to either add the element into the array or remove the elements from the array.

//Add using splice: 
arr = ['b','e','a','o','p','n','r'];
console.log(arr);
arr.splice(2,2,'rj','nodejs');
console.log(arr);

//Remove using splice
arr = ['b','e','a','o','p','n','r'];
console.log(arr);
arr.splice(2,2);
console.log(arr)